void main() {
  int x = 12;
  int y = 20;

  if (x <= y) {
    print('true');
  } else {
    print('false');
  }

  if (x < y) {
    print('true');
  } else {
    print('false');
  }

  if (x >= y) {
    print('true');
  } else {
    print('false');
  }

  if (x < y) {
    print('true');
  } else {
    print('false');
  }

  if (x == y) {
    print('true');
  } else {
    print('false');
  }

  if (x != y) {
    print('true');
  } else {
    print('false');
  }
}
