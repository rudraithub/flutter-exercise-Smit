void main() {
  int week = 7;
  switch (week = 2) {
    case 1:
      print('monday');
      break;

    case 2:
      print('tuesdays');
      break;

    case 3:
      print('wednesday');
      break;

    case 4:
      print('thursday');
      break;

    case 5:
      print('friday');
      break;

    case 6:
      print('saturday');
      break;
    default:
      print('sunday');
      break;
  }
}
